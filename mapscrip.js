function addPath(latlongarray) {

    if (map.getLayer('line')) {

        map.removeLayer('line');
        map.removeSource('line');
    }

    map.flyTo({
        center: latlongarray[0],
        zoom: 20,
        speed: 1.2,
        curve: 1
    });

    map.fitBounds([
        latlongarray[0], // southwestern corner of the bounds
        latlongarray[latlongarray.length - 1] // northeastern corner of the bounds
    ]);



    const ls = {
        'type': 'Feature',
        'properties': {},
        'geometry': {
            "coordinates": latlongarray,
            "type": "LineString"
        }
    }

    const geojson = {
        'type': 'FeatureCollection',
        'features': [
            ls
        ]
    };
    map.addSource('line', {
        type: 'geojson',
        lineMetrics: true,
        data: geojson
    });
    map.addLayer({
        type: 'line',
        source: 'line',
        id: 'line',
        paint: {
            'line-color': 'red',
            'line-width': 5,
            // 'line-gradient' must be specified using an expression
            // with the special 'line-progress' property
            'line-gradient': [
                'interpolate',
                ['linear'],
                ['line-progress'],
                0.0000212287, 'rgb(255,0,0)',
                0.5, 'rgb(0,255,0)',
                1, 'rgb(0,0,255)'



            ]
        },
        layout: {
            'line-cap': 'round',
            'line-join': 'round'
        }
    });

}